package exercise;

public class Q4 {

	public static void main(String[] args) {
		int var1 = 10;
		double var2 = 2.0;
		
		
		// 사칙 연산 결과를 정수로 출력
		int add = (int)(var1 + var2);
		System.out.println(add);
		
		int sub = (int)(var1 - var2);
		System.out.println(sub);
		
		int mul = (int)(var1 * var2);
		System.out.println(mul);
		
		int div = (int)(var1 / var2);
		System.out.println(div);

		System.out.println((int)(var1 + var2));
		System.out.println((int)(var1 - var2));
		System.out.println((int)(var1 * var2));
		System.out.println((int)(var1 / var2));

	}

}
